#include <stdio.h>
#include <string.h>

struct Books {
	char title[50];
	char author[50];
	char subject[100];
	int book_id;
};

int main() {
	struct Books Book1;
	struct Books Book2;

	strcpy_s(Book1.title, "C Programming");
	strcpy_s(Book1.author, "Nuha Ali");
	strcpy_s(Book1.subject, "C Progammning Tutorial");
	Book1.book_id = 6495700;

	printf("Book1 title : %s\n", Book1.title);
	printf("Book1 author : %s\n", Book1.author);
	printf("Book1 subject : %s\n", Book1.subject);
	printf("Book1 book_id : %d\n", Book1.book_id);

	printf("Book 2 title : %s\n", Book2.title);
	printf("Book 2 author : %s\n", Book2.author);
	printf("Book 2 subject : %s\n", Book2.subject);
	printf("Book 2 book_id : %d\n", Book2.book_id);

}


